/*
01) Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração,
multiplicação e divisão desses valores.
*/
function operacoes(a,b){
    console.log(`A adição entre ${a} e ${b} resulta em ${a+b}`);
    console.log(`A subtração entre ${a} e ${b} resulta em ${a-b}`);
    console.log(`A multiplicação entre ${a} e ${b} resulta em ${a*b}`);
    console.log(`A divisão entre ${a} e ${b} resulta em ${a/b}`);
}

operacoes(10, 2)
